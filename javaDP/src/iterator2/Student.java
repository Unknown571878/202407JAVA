package iterator2;

public class Student {
	public String name;
	public String hakbun;
	public double java;
	
	public Student(String name, String hakbun, double java) {
		super();
		this.name = name;
		this.hakbun = hakbun;
		this.java = java;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
